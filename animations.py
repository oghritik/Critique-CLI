import curses, time

def bounce(stdscr):
    stdscr.clear()
    msg = "Hello!"
    x = y = 0
    dx = dy = 1
    while time.time() < time.time() + 1:  # Run for 10 seconds
        stdscr.clear()
        stdscr.addstr(y, x, msg)
        stdscr.refresh()
        y, x = y + dy, x + dx
        if y == 0 or y == curses.LINES - 1:
            dy *= -1
        if x == 0 or x == curses.COLS - len(msg):
            dx *= -1
        time.sleep(0.05)

curses.wrapper(bounce)

